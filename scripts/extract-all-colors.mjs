/**
 * Extrage culoarea dominantă (non-alb/negru/gri) pentru fiecare logo unic din public/logos/.
 * Output: src/data/dominant-colors.js
 *
 * Rulare: node scripts/extract-all-colors.mjs
 */

import sharp from 'sharp'
import { readdirSync, writeFileSync } from 'fs'
import { resolve, join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const LOGOS_DIR = resolve(__dirname, '../public/logos')
const OUT_FILE  = resolve(__dirname, '../src/data/dominant-colors.js')

function rgbToHsl(r, g, b) {
  r /= 255; g /= 255; b /= 255
  const max = Math.max(r, g, b), min = Math.min(r, g, b)
  let h = 0, s = 0
  const l = (max + min) / 2
  if (max !== min) {
    const d = max - min
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min)
    switch (max) {
      case r: h = ((g - b) / d + (g < b ? 6 : 0)) / 6; break
      case g: h = ((b - r) / d + 2) / 6; break
      case b: h = ((r - g) / d + 4) / 6; break
    }
  }
  return [h * 360, s * 100, l * 100]
}

async function extractDominantColor(filePath) {
  const { data } = await sharp(filePath).ensureAlpha().raw().toBuffer({ resolveWithObject: true })
  const STEP = 5, N = 360 / STEP
  const buckets = new Array(N).fill(0)

  for (let i = 0; i < data.length; i += 4) {
    if (data[i + 3] < 64) continue
    const [h, s, l] = rgbToHsl(data[i], data[i + 1], data[i + 2])
    if (l > 87 && s < 20) continue
    if (l < 13) continue
    if (s < 12) continue
    buckets[Math.floor(h / STEP) % N]++
  }

  const score = i => [-2, -1, 0, 1, 2].reduce((s, d) => s + buckets[(i + d + N) % N], 0)
  let peakIdx = 0
  for (let i = 1; i < N; i++) if (score(i) > score(peakIdx)) peakIdx = i

  const peakHue = peakIdx * STEP + STEP / 2
  let totalS = 0, totalL = 0, count = 0

  for (let i = 0; i < data.length; i += 4) {
    if (data[i + 3] < 64) continue
    const [h, s, l] = rgbToHsl(data[i], data[i + 1], data[i + 2])
    if (l > 87 && s < 20) continue
    if (l < 13) continue
    if (s < 12) continue
    const diff = Math.abs(h - peakHue)
    if (Math.min(diff, 360 - diff) < 25) { totalS += s; totalL += l; count++ }
  }

  if (count === 0) return null
  return { h: Math.round(peakHue), s: Math.round(totalS / count), l: Math.round(totalL / count) }
}

// ── Colectează toate logo-urile unice (deduplicat după slug) ─────────────
const seen = new Map() // slug -> filePath (primul găsit)
for (const entry of readdirSync(LOGOS_DIR, { withFileTypes: true })) {
  if (!entry.isDirectory()) continue
  const colPath = join(LOGOS_DIR, entry.name)
  for (const file of readdirSync(colPath)) {
    if (!file.endsWith('.png')) continue
    const slug = file.replace('.football-logos.cc.png', '')
    if (!seen.has(slug)) seen.set(slug, join(colPath, file))
  }
}

console.log(`Procesez ${seen.size} logo-uri unice...\n`)

const results = {}
let done = 0
for (const [slug, filePath] of seen) {
  try {
    results[slug] = await extractDominantColor(filePath)
  } catch {
    results[slug] = null
  }
  done++
  if (done % 50 === 0 || done === seen.size) {
    process.stdout.write(`  ${done}/${seen.size}\r`)
  }
}

const nullCount = Object.values(results).filter(v => v === null).length
console.log(`\nExtrase: ${seen.size - nullCount} cu culoare, ${nullCount} monocrome (null)`)

// ── Scrie fișierul de output ─────────────────────────────────────────────
const lines = Object.entries(results)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([slug, c]) =>
    c ? `  '${slug}': { h: ${c.h}, s: ${c.s}, l: ${c.l} },`
      : `  '${slug}': null,`
  )

writeFileSync(
  OUT_FILE,
  `// Auto-generated by scripts/extract-all-colors.mjs — nu edita manual\nexport const dominantColors = {\n${lines.join('\n')}\n}\n`
)
console.log(`Output: ${OUT_FILE}`)
